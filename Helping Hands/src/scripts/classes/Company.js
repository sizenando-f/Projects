export class Company {
  constructor(
    name,
    email,
    password,
    address,
    city,
    state,
    zip,
    website,
    contact,
    description
  ) {
    this.name = name;
    this.email = email;
    this.password = password;
    this.address = address;
    this.city = city;
    this.state = state;
    this.zip = zip;
    this.website = website;
    this.contact = contact;
    this.description = description;
  }
}
